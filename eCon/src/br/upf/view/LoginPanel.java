    private void logar() {
        String usuario = txtUser.getText();
        String senha = txtPwd.getText();
        if (new LoginController().login(usuario, senha) == 1) {
            this.dispose();
            new Main().principal();
        } else {
            txtUser.setText("");
            txtPwd.setText("");
        }

    }


