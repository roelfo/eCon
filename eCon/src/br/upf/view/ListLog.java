    private void preencheTabela(){
        List<Log> lista = new GravaLog().todosLog();
        
        DefaultTableModel modelo = (DefaultTableModel) tableLog.getModel();
        for(Log log : lista){
            modelo.addRow(new Object[] {log.getId(), log.getAcao(), log.getClasse(), log.getSql(), log.getTimestamp(), log.getUsuario().getNome()});
        }
    }
    
    private void colunas(){
        String[] colunas = {"ID", "Ação", "Classe", "SQL", "Timestamp", "Usuário"};
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        for(String s : colunas){
            modelo.addColumn(s);
        }
        tableLog.setModel(modelo);
    }

